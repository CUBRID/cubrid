char *
trim (char *str)
{
  char *p, *s;
  int len = 0;

  if (str != NULL && *str != '\0')
    {
      for (s = str; char_isspace2 (*s); s++)
	{
	  /* empty */ ;
	}
      if (*s == '\0')
	{
	  *str = '\0';
	  return (str);
	}

      /* *s must be a non-white char */
      for (p = s; *p != '\0'; p++)
	{
	  /* empty */ ;
	}
      for (p--; char_isspace2 (*p); p--)
	{
	  /* empty */ ;
	}

      len = (int) (p - s) + 1;
      if (s != str)
	{
	  memmove (str, s, len);
	}
      str[len] = '\0';
    }

  return (str);
}

