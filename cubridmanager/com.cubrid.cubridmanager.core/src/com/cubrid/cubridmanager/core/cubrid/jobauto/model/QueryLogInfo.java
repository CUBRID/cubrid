package com.cubrid.cubridmanager.core.cubrid.jobauto.model;

/**
 * A model class that includes all the query log info
 * 
 *  @author lizhiqiang 2009-4-21
 */
public class QueryLogInfo {
	private String dbname;
	private String username;
	private String query_id;
	private String error_time;
	private String error_code;
	private String error_desc;

	public String getDbname() {
		return dbname;
	}

	public void setDbname(String dbname) {
		this.dbname = dbname;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getQuery_id() {
		return query_id;
	}

	public void setQuery_id(String query_id) {
		this.query_id = query_id;
	}

	public String getError_time() {
		return error_time;
	}

	public void setError_time(String error_time) {
		this.error_time = error_time;
	}

	public String getError_code() {
		return error_code;
	}

	public void setError_code(String error_code) {
		this.error_code = error_code;
	}

	public String getError_desc() {
		return error_desc;
	}

	public void setError_desc(String error_desc) {
		this.error_desc = error_desc;
	}
}
